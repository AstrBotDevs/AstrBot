# 话题总结提示词（与 MaiBot hippo_topic_summary_prompt 一致）
TOPIC_SUMMARY_PROMPT = """
请基于以下话题，对聊天记录片段进行概括，提取以下信息：

**话题**：{topic}

**要求**：
1. 关键词：提取与话题相关的关键词，用列表形式返回（3-10个关键词）
2. 概括：对这段话的平文本概括（50-200字），要求：
   - 仔细地转述发生的事件和聊天内容；
   - 可以适当摘取聊天记录中的原文；
   - 重点突出事件的发展过程和结果；
   - 围绕话题这个中心进行概括。
3. 关键信息：提取话题中的关键信息点，用列表形式返回（3-8个关键信息点），每个关键信息点应该简洁明了。

请以JSON格式返回，格式如下：
{{
    "keywords": ["关键词1", "关键词2", ...],
    "summary": "概括内容",
    "key_point": ["关键信息1", "关键信息2", ...]
}}

聊天记录：
{original_text}

请直接返回JSON，不要包含其他内容。
"""

